import pandas as pd 
from sklearn.preprocessing import LabelEncoder 
# ──────────────────────────────────────
# 샘플 데이터 — 고객 설문 데이터
# ────────────────────────────────────── 
data = { 
    "이름": ["김철수", "이영희", "박민수", "최수진", "정우성"], 
    "나이": [28, 35, 42, 23, 31], 
    "성별": ["남", "여", "남", "여", "남"], 
    "만족도": ["매우만족", "만족", "보통", "만족", "불만족"], 
    "구매횟수": [5, 12, 3, 8, 1], 
    "지역": ["서울", "부산", "서울", "대전", "부산"], 
    "우편번호": [10001, 20002, 10003, 30001, 20004] 
} 
df = pd.DataFrame(data) 

# ──────────────────────────────────────
# Step 1: 변수 타입 판별
# ────────────────────────────────────── 
print("=" * 60) 
print("Step 1: 변수 타입 자동 판별") 
print("=" * 60) 

for col in df.columns: 
    n_unique = df[col].nunique() 
    dtype = df[col].dtype 
    if dtype in ["int64", "float64"] and n_unique <= 10: 
        label = "⚠ 숫자이지만 범주형 가능성" 
    elif dtype == "object": 
        label = "📋 범주형" 
    else: 
        label = "📊 수치형" 

    print(f"  {col:8s} | 고유값 {n_unique}개 | {dtype} | {label}") 

print("\n💡 '우편번호'는 숫자이지만 평균에 의미가 없으므로 범주형입니다.") 
print(f"   우편번호 평균 = {df['우편번호'].mean():.0f} ← 의미 없는 숫자!") 

# ──────────────────────────────────────
# Step 2: 원-핫 인코딩 (명목형 — 순서 없음)
# ────────────────────────────────────── 
print("\n" + "=" * 60) 
print("Step 2: 원-핫 인코딩 (성별, 지역)") 
print("=" * 60) 

df_onehot = pd.get_dummies(df, columns=["성별", "지역"], dtype=int) 
print(df_onehot[["이름", "성별_남", "성별_여", "지역_대전", "지역_부산", "지역_서울"]]) 

# ──────────────────────────────────────
# Step 3: 레이블 인코딩 (순서형 — 순서 있음)
# ────────────────────────────────────── 
print("\n" + "=" * 60) 
print("Step 3: 레이블 인코딩 (만족도)") 
print("=" * 60) 

# 수동 매핑 (순서를 명확히 지정) 
satisfaction_map = {"불만족": 0, "보통": 1, "만족": 2, "매우만족": 3} 
df["만족도_코드"] = df["만족도"].map(satisfaction_map)
print(df[["이름", "만족도", "만족도_코드"]])

# ──────────────────────────────────────
# Step 4: 머신러닝에 넣을 수 있는 최종 형태
# ────────────────────────────────────── 
print("\n" + "=" * 60) 
print("Step 4: 최종 피처 선택") 
print("=" * 60) 

# 모델에 넣을 피처 (숫자만) 
features = df_onehot[["나이", "구매횟수"]].copy() 
features["만족도_코드"] = df["만족도"].map(satisfaction_map) 
features = pd.concat([ 
    features, 
    df_onehot[["성별_남", "성별_여", "지역_대전", "지역_부산", "지역_서울"]] 
], axis=1) 
print(features) 
print(f"\n→ 문자열이 없는 숫자 데이터 = 머신러닝 모델 입력 준비 완료!") 